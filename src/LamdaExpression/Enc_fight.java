package LamdaExpression;

public class Enc_fight {
private String name;
private Integer id;
private Double sal;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public Integer getId() {
	return id;
}
public void setId(Integer id) {
	this.id = id;
}
public Double getSal() {
	return sal;
}
public void setSal(Double sal) {
	this.sal = sal;
}
public Enc_fight(String name, Integer id, Double sal) {
	super();
	this.name = name;
	this.id = id;
	this.sal = sal;
}

}
